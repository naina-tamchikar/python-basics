# Topic : Logical Operators
# Logical Operators conditions ko
# combine karne ke liye use hote hain
# =====================================

age = 20
has_id = True

# AND Operator
# Dono conditions True honi chahiye

print(age >= 18 and has_id)

# OR Operator
# Ek bhi condition True ho to result True

print(age < 18 or has_id)

# NOT Operator
# True ko False aur False ko True bana deta hai

print(not has_id)

# AND = Dono conditions True honi chahiye

# OR = Ek bhi condition True ho to True

# NOT = Ulta kar deta hai



# ===========================================
# Shorthand If Statement in Python
# ===========================================

# Shorthand If is used to write a simple
# 'if' statement in a single line.

# Syntax:
# if condition: statement

# Example 1
age = 20

if age >= 18: print("You are eligible to vote.")

print("-" * 30)

# Example 2
marks = 90

if marks >= 80: print("Excellent Performance!")

print("-" * 30)

# Example 3 (User Input)
number = int(input("Enter a number: "))

if number > 0: print("Positive Number")

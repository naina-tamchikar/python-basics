# User se age le rahe hain
age = input("Enter your age : ")

# Input ka type check kar rahe hain
print(type(age))

# String ko Integer me convert kar rahe hain
age = int(age)

# Conversion ke baad type check kar rahe hain
print(type(age))

# Age print kar rahe hain
print("Your Age is :", age)



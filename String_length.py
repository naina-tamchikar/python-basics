# len() function string me total characters count karta hai.

name = "Naina"

# String ki length nikalna
print("Name:", name)
print("Length of name:", len(name))

print("--------------------")

city = "Mumbai"

print("City:", city)
print("Length of city:", len(city))

print("--------------------")

# Spaces bhi count hote hain

full_name = "Naina Shaikh"

print("Full Name:", full_name)
print("Length of full name:", len(full_name))

print("--------------------")

# User se input lena aur length nikalna

text = input("Enter any text: ")

print("You entered:", text)
print("Total characters:", len(text))


# ==========================================
# Escape Characters in Python Strings
# ==========================================

# Escape characters are used to add special
# formatting inside a string.

# \n -> New Line
print("Hello\nWorld")

print("--------------------")

# \t -> Tab Space
print("Python\tProgramming")

print("--------------------")

# Using quotes inside a string

print("My name is \"Naina\"")

print('I am learning \'Python\'')

print("--------------------")

# Multiple escape characters together

print("Name:\tNaina")
print("City:\tMumbai")
print("Country:\tIndia")
a = 10
b = 20

# Equal To
print("a == b :", a == b)

# Not Equal To
print("a != b :", a != b)

# Greater Than
print("a > b :", a > b)

# Less Than
print("a < b :", a < b)

# Greater Than Equal To
print("a >= b :", a >= b)

# Less Than Equal To
print("a <= b :", a <= b)

